{
    int i;
    for(i=1;i<=n;i++)
    {
        if(vis[i]==0&&g[u][i]==1)
        {
            vis[i]=1;
            if(p[i]==0||find(p[i])==1)
            {
                p[i]=u;
                return 1;
            }
        }
    }
    return 0;
}

