ll e_gcd(ll a,ll b,ll &x,ll &y)
{	if (b==0)
	{	x=1;
		y=0;
		return a;
	}
	ll ans=e_gcd(b,a%b,x,y);
	ll tmp=x;
	x=y;
	y=tmp-(a/b)*y;
	return ans;
}
